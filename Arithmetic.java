public class Arithmetic{

    public static void main (String args[]){

        int a =10;
        int b = 20;

        int c = a+b;
        System.out.println(c);

        int d=b-a;
        int e=a*b;
        int f=b/a;
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);

        int x = 10;
        int y = 3;
        int z = x%y;

        System.out.println(z);

        //Increment
        x++;
        System.out.println(x);

        //decrement
        y--;
        System.out.println(y);
    }
}